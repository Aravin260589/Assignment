
public class Course
{
	String CourseCode;
	String CourseName;

	
public Course(String CourseCode, String CourseName) 
		{
	        this.CourseName = CourseName;
	        this.CourseCode = CourseCode;
	    }

///////////////////	
public  String getCourseCode()
{
	return CourseCode;
}
////////////////////
public  String getCourseName()
{
	return CourseName;
}
//////////
}



